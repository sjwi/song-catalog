$(document).ready(function(){
	$(document).on('change','.set-list-container select[name="defaultKey"]',function(e){
		$('#overlay').addClass("overlay");
		$(this).siblings('.confirm-keychange').show();
		$(this).siblings('.confirm-keychange').find('.transpose-global').attr("data-key",$(this).val());
		$(this).siblings('.confirm-keychange').find('.transpose-global').attr("data-songid",$(this).closest('.set-song-row').data('setsongid'));
		$(this).siblings('.confirm-keychange').find('.transpose-global').attr("data-setid",$(this).closest('.set-song-row').data('setid'));
	})
	$(document).on('click','.transpose-global',function(e){
		changeKeyForSetSong($(this).attr("data-key"),$(this).attr("data-songid"),$(this).attr('data-setid'));
		$('#overlay').removeClass("overlay");
		$(this).closest('.confirm-keychange').hide();
	});
	$(document).on('click','.transpose-local',function(e){
		localSetKeyChange($(this).closest('.set-list-container'));
		$('#overlay').removeClass("overlay");
		$(this).closest('.confirm-keychange').hide();
	});
})